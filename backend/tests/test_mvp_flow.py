def test_missing_session_returns_404(client: TestClient) -> None:
    """Rollback on a non-existent session returns 404."""
    resp = client.post(
        "/api/v1/sessions/nonexistent-sess/rollback",
        json={"finding_id": "f1", "reason": "test"},
    )
    assert resp.status_code == 404


def test_verify_missing_session_returns_404(client: TestClient) -> None:
    """Verify on a non-existent session returns 404."""
    resp = client.post("/api/v1/verify/simple", json={"session_id": "nonexistent-sess"})
    assert resp.status_code == 404


def test_rollback_nonexistent_finding_returns_gracefully(client: TestClient) -> None:
    """Rolling back a finding that does not exist in the session must not
    crash — the controller validates existence and returns current state."""
    data = _create_task(client, "user007", "Nonexistent finding test")

    resp = client.post(
        f"/api/v1/sessions/{data['session_id']}/rollback",
        json={"finding_id": "f_does_not_exist", "reason": "test"},
    )
    # The orchestrator catches RollbackError and returns 200 with current state
    assert resp.status_code == 200, resp.text
    # The rollbackable list should be unchanged
    result = resp.json()
    opts = result.get("rollback_options", {})
    rollbackable = opts.get("rollbackable_findings", [])
    assert len(rollbackable) >= 1
